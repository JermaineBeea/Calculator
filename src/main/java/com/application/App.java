package com.application;

/**
 * Main application class for the Calculator project.
 */
public class App {

    public static int add(int a, int b) {
        return a + b;
    }

    public static int subtract(int a, int b) {
        return a - b;
    }

    public static int product(int a, int b) {
        return a * b;
    }
    
}
